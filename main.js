console.log('Probando métodos push(), shift(), pop(), unshift()');

// #### DECLARACIÓN DEL ARREGLO
var animales = ['perro', 'gato'];
console.log('Arreglo a manejar: ', animales);

// Push para agregar elefante al final del arreglo
console.log('Aplicando método push()');
animales.push('elefante');
console.log(animales);

// Pop para eliminar el último elemento del arreglo.
console.log('Aplicando método pop()');
animales.pop();
console.log(animales);

// Unshift() para agregar un elemento al principio del arreglo.
console.log('Aplicando método unshift()');
animales.unshift('jirafa');
console.log(animales);

// Pop para quitar el primer elemento de un arreglo.
console.log('Aplicando método pop()');
animales.pop();
console.log(animales);

